package com.ajou.android.acma;

public class Student {
    private String id;
    private String password;
    private String name;
    private int grade;
    private int sex;

    public Student(String id, String password, String name, int grade, int sex) {
        this.id = id;
        this.password = password;
        this.name = name;
        this.grade = grade;
        this.sex = sex;
    }


}
